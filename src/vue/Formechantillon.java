private void modifierechantillons() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int Id_echantillons = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE echantillons SET Methode_prelevement = ?, Date_prelevement = ?, Type_echa_prelever = ?, Id_demandes = ? WHERE Id_echantillons = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, jTmethprel.getText());
                java.util.Date utilDate = jDateChooser1.getDate();
                java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
                pstmt.setDate(2, sqlDate);
                pstmt.setString(3, jTtypeecha.getText());
                
                String selectedDemande = jComboBox1.getSelectedItem().toString();
                String[] parts = selectedDemande.split(" ", 2);
                int iddemandes = Integer.parseInt(parts[0].trim());
                pstmt.setInt(4, iddemandes);
                
                pstmt.setInt(5, Id_echantillons);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Echantillon modifiée avec succès");
                visualiserechantillonData();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un échantillon à modifier");
        }
    }
 private void supprimerechantillon() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String echantillonsId = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM echantillons WHERE Id_echantillons = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, echantillonsId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "échantillon supprimée avec succès");
                visualiserechantillonData();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un échantillon à supprimer");
        }
    }
 private void  rechercherechantillon() {
        String searchTerm = jTrechercher.getText();
        String sql = "SELECT * FROM echantillons WHERE Methode_prelevement LIKE ? OR Date_prelevement LIKE ? OR Type_echa_prelever LIKE ? OR Id_demandes LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
             
            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("Id_echantillons"),
                        rs.getString("Methode_prelevement"),
                        rs.getString("Date_prelevement"),
                        rs.getString("Type_echa_prelever"),
                        rs.getInt("Id_demandes"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }


