private void enregistrerdemande() {
    String sql = "INSERT INTO echantillons(Methode_prelevement, Date_prelevement, Type_echa_prelever, Id_demandes) VALUES(?, ?, ?, ?)";

    try (Connection conn = this.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, jTmethprel.getText());

        java.util.Date utilDate = jDateChooser1.getDate();
        java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
        pstmt.setDate(2, sqlDate);

        pstmt.setString(3, jTtypeecha.getText());

        String selectedDemande = jComboBox1.getSelectedItem().toString();
        String[] parts = selectedDemande.split(" ", 2); // Diviser en deux parties seulement
        int iddemandes = Integer.parseInt(parts[0].trim()); // Extraire l'ID
        pstmt.setInt(4, iddemandes);

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Echantillon enregistré avec succès");

    } catch (SQLException e) {
        System.out.println(e.getMessage());
    } catch (NumberFormatException e) {
        System.out.println("Erreur de format de nombre pour l'ID : " + e.getMessage());
    }
}

private void visualiserechantillonData() {
        String sql = "SELECT * FROM echantillons";
        
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("Id_echantillons"),
                     rs.getString("Methode_prelevement"),
                    rs.getString("Date_prelevement"),
                    rs.getString("Type_echa_prelever"),
                    rs.getInt("Id_demandes"),
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage());
        }
    }
