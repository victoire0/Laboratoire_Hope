private void enregistrerlaborantin() {
    String sql = "INSERT INTO Laborantin(Nom, Specialite, Id_echantillons) VALUES(?, ?, ?)";

    try (Connection conn = this.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, jTnom.getText());
        pstmt.setString(2, jTspecialite.getText());
        
        String selectedEchantillon = jComboBox1.getSelectedItem().toString();
        String[] parts = selectedEchantillon.split(" ", 2); // Diviser en deux parties seulement
        int idechantillons = Integer.parseInt(parts[0].trim()); // Extraire l'ID
        pstmt.setInt(3, idechantillons);

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Laborantin enregistré avec succès");

    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }
}
private void visualiserlaborantinData() {
        String sql = "SELECT * FROM laborantin";
        
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("Id_laborantins"),
                    rs.getString("Nom"),
                    rs.getString("Specialite"),
                    rs.getInt("Id_echantillons"),
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage());
        }
    }
