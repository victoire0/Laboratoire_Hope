private void modifierlaborantin() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int Id_laborantins = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE laborantin SET Nom = ?, Specialite = ?, Id_echantillons = ? WHERE Id_laborantins = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, jTnom.getText());
                pstmt.setString(2, jTspecialite.getText());
                
                String selectedDemande = jComboBox1.getSelectedItem().toString();
                String[] parts = selectedDemande.split(" ", 2);
                int idechantillons = Integer.parseInt(parts[0].trim());
                pstmt.setInt(3, idechantillons);
                
                pstmt.setInt(4, Id_laborantins);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Laborantin modifié avec succès");
                visualiserlaborantinData();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un Laborantin à modifier");
        }
    }
private void supprimerlaborantin() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String echantillonsId = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM laborantin WHERE Id_laborantins = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, echantillonsId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Laborantin supprimé avec succès");
                visualiserlaborantinData();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un Laborantin à supprimer");
        }
    }
private void  rechercherlaborantin() {
        String searchTerm = jTrechercher.getText();
        String sql = "SELECT * FROM echantillons WHERE Methode_prelevement LIKE ? OR Date_prelevement LIKE ? OR Type_echa_prelever LIKE ? OR Id_demandes LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
             
            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("Id_echantillons"),
                        rs.getString("Methode_prelevement"),
                        rs.getString("Date_prelevement"),
                        rs.getString("Type_echa_prelever"),
                        rs.getInt("Id_demandes"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }


