private void enregistrerpatient() {
    String sql = "INSERT INTO Patients(Nom, Prenom, Tel, Id_demandes) VALUES(?, ?, ?, ?)";

    try (Connection conn = this.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {

        pstmt.setString(1, jTnompat.getText());
        pstmt.setString(2, jTprenompat.getText());
        pstmt.setString(3, jTtelpat.getText());
        
        String selectedEchantillon = jComboBox1.getSelectedItem().toString();
        String[] parts = selectedEchantillon.split(" ", 2); // Diviser en deux parties seulement
        int idechantillons = Integer.parseInt(parts[0].trim()); // Extraire l'ID
        pstmt.setInt(4, idechantillons);

        pstmt.executeUpdate();
        JOptionPane.showMessageDialog(null, "Patient enregistré avec succès");

    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }
}
private void visualiserpatientData() {
        String sql = "SELECT * FROM patients";
        
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                Object[] row = {
                    rs.getInt("Id_patients"),
                    rs.getString("Nom"),
                    rs.getString("Prenom"),
                    rs.getInt("Tel"),
                    rs.getInt("Id_demandes"),
                };
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erreur lors du chargement des données : " + e.getMessage());
        }
    }
