private void modifierpatient() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            int Id_patients = (int) jTable1.getValueAt(selectedRow, 0);
            String sql = "UPDATE patients SET Nom = ?, Prenom = ?, Tel = ?, Id_demandes = ? WHERE Id_patients = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, jTnompat.getText());
                pstmt.setString(2, jTprenompat.getText());
                pstmt.setString(3, jTtelpat.getText());
                
                String selectedDemande = jComboBox1.getSelectedItem().toString();
                String[] parts = selectedDemande.split(" ", 2);
                int idechantillons = Integer.parseInt(parts[0].trim());
                pstmt.setInt(4, idechantillons);
                
                pstmt.setInt(5, Id_patients);

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Patient modifié avec succès");
                visualiserpatientData();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Erreur lors de la modification des données : " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Veuillez sélectionner un Patient à modifier");
        }
    }
private void supprimerpatient() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            String echantillonsId = jTable1.getValueAt(selectedRow, 0).toString();
            String sql = "DELETE FROM patients WHERE Id_patients = ?";

            try (Connection conn = this.connect();
                 PreparedStatement pstmt = conn.prepareStatement(sql)) {

                pstmt.setString(1, echantillonsId);
                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Patient supprimé avec succès");
                visualiserpatientData();
            } catch (SQLException e) {
                System.out.println(e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez sélectionner un Laborantin à supprimer");
        }
    }
private void  rechercherpatient() {
        String searchTerm = jTrechercher.getText();
        String sql = "SELECT * FROM patients WHERE  Nom LIKE ? OR Prenom LIKE ? OR Tel LIKE ? OR Id_demandes LIKE ?";
        
        try (Connection conn = this.connect();
             PreparedStatement pstmt = conn.prepareStatement(sql)) {
            
            String searchPattern = "%" + searchTerm + "%";
            pstmt.setString(1, searchPattern);
            pstmt.setString(2, searchPattern);
            pstmt.setString(3, searchPattern);
            pstmt.setString(4, searchPattern);
             
            ResultSet rs = pstmt.executeQuery();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                        rs.getInt("Id_patients"),
                        rs.getString("Nom"),
                        rs.getString("Prenom"),
                        rs.getString("Tel"),
                        rs.getInt("Id_demandes"),
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

    
    
