private Connection connect() {
    String url = "jdbc:mysql://localhost:3306/laboratoire_hopital_hope";
    String user = "root";
    String password = "";
    Connection conn = null;
    try {
        conn = DriverManager.getConnection(url, user, password);
    } catch (SQLException e) {
        System.out.println(e.getMessage());
    }
    return conn;
}
private void infoDemandes() {
        String sql = "SELECT Id_demandes, Medecin_demandeur FROM Demandes";
        try (Connection conn = this.connect();
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
            while (rs.next()) {
                String idMeddemandeurDemande = rs.getInt("Id_demandes") + " / " + rs.getString("Medecin_demandeur");
                model.addElement(idMeddemandeurDemande);
            }
            jComboBox1.setModel(model);

        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
